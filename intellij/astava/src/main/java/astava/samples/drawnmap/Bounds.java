package astava.samples.drawnmap;

public class Bounds {
    public final Object value;
    public final int x;
    public final int y;
    public final int width;
    public final int height;

    public Bounds(Object value, int x, int y, int width, int height) {
        this.value = value;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
